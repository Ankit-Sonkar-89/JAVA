import java.util.*;
public class Loops1 {
    public static void main ( String args [] ) {

        // counter = 0; counter <11; counter = counter + 1; 
        // counter = counter + 1 => counter++ ( increment operator )

        for ( int i = 0; i < 11; i++ ) {
            System.out.println(i);
        }
    }
}